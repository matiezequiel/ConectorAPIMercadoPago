package com.mercadopago.model;

public class QRBox {
	private String image;
	private String template_document;
	private String template_image;
	
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getTemplate_document() {
		return template_document;
	}
	public void setTemplate_document(String template_document) {
		this.template_document = template_document;
	}
	public String getTemplate_image() {
		return template_image;
	}
	public void setTemplate_image(String template_image) {
		this.template_image = template_image;
	}
}
